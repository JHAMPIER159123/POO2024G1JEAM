    public void listarClientes() {
        List<Cliente> listarCleintes = clienteService.listarEntidad();
        if (listarCleintes != null) {
            jTable1.setAutoCreateRowSorter(true);
            modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setNumRows(0);
            Object[] ob = new Object[4];
            for (int i = 0; i < listarCleintes.size(); i++) {
                ob[0] = i + 1;
                ob[1] = listarCleintes.get(i).getCantidad();
                ob[2] = listarCleintes.get(i).getModelo();
                ob[3] = listarCleintes.get(i).getMarca();
                modelo.addRow(ob);
            }
        }
        jTable1.setModel(modelo);
    }

    public void resetForm() {
        txtCantidad.setText("");
        txtModelo.setText("");
        cbxMarca.setSelectedIndex(0);
        txtCantidad.requestFocus();
    }

    private void paintForm() {
        if (jTable1.getSelectedRow() != -1) {
            modelo = (DefaultTableModel) jTable1.getModel();
            int rowx = jTable1.getSelectedRow();
            Object valor = jTable1.getValueAt(rowx, 1);
            //ClienteTO filax = (ClienteTO) modelo.getRow(jTable1.getSelectedRow()); 
            Cliente d = clienteService.buscarCliente(valor.toString());
            txtCantidad.setText(d.getCantidad());
            txtModelo.setText(d.getModelo());
            cbxMarca.setSelectedItem(d.getMarca());
            txtCantidad.setEditable(false);
            btnRegistrar.setText("Modificar");
            //guardarButton.setToolTipText("MODIFICAR");
        } else {
            txtModelo.setEditable(true);
        }
    }

